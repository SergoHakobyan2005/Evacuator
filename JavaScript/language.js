let allLang = ['AM', 'RU', 'EN'];
let lng = document.querySelector('.lng');
let dropdown = document.querySelector('.dropdown');
let btds = document.querySelectorAll('.btn');
let sp = document.querySelector('.lang-change span');

lng.addEventListener('click', function (e) {
    dropdown.classList.toggle('active-dropdown');
    e.stopPropagation();
})

btds.forEach(item => {
    item.addEventListener('click', () => {
        let lad = item.id;
        sp.innerHTML = lad;
        console.log(lad);
        location.href = window.location.pathname + '#' + lad;
        location.reload();
    })
    function changeLang() {
        let hash = window.location.hash;
        hash = hash.substr(1);
        console.log(hash);
        if (!allLang.includes(hash)) {
            location.href = window.location.pathname + '#AM';
            location.reload();
        }
        sp.innerHTML = hash;
        for (let key in languageArr) {
            let all = document.querySelector('.language-' + key);
            if (all) {
                all.innerHTML = languageArr[key][hash];
            }
        }
    }
    changeLang();
})

window.addEventListener('click', function (e) {
    e.stopPropagation();
    dropdown.classList.remove('active-dropdown');
})

if (sp.innerHTML == btds[0].id) {
    lng.classList.toggle('lng-flag-1');
} else if (sp.innerHTML == btds[1].id) {
    lng.classList.toggle('lng-flag-2');
} else if (sp.innerHTML == btds[2].id) {
    lng.classList.toggle('lng-flag-3');
}